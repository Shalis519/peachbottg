def _get_branches(birth_date: date, birth_time: time) -> tuple[int, int]:
    """
    Получить индексы Земных ветвей года и дня через lunar_python.
    lunar_python учитывает точную дату Личунь (и других солнечных сроков)
    для каждого конкретного года.
    """
    solar = Solar.fromYmdHms(
        birth_date.year, birth_date.month, birth_date.day,
        birth_time.hour, birth_time.minute, 0
    )
    lunar = solar.getLunar()
    bazi = lunar.getEightChar()

    year_zh = bazi.getYearZhi()
    day_zh  = bazi.getDayZhi()

    year_idx = BRANCH_ZH_TO_IDX[year_zh]
    day_idx  = BRANCH_ZH_TO_IDX[day_zh]

    return year_idx, day_idx


def get_peach_flower_info(birth_date: date, birth_time: time, city: str) -> tuple:
    """Сформировать итоговый текст с результатами расчёта.
    Возвращает (text, year_animal, day_animal)."""

    y_idx, d_idx = _get_branches(birth_date, birth_time)

    y_ru, y_zh = BRANCH_RU[y_idx], BRANCH_ZH[y_idx]
    d_ru, d_zh = BRANCH_RU[d_idx], BRANCH_ZH[d_idx]

    y_animal, y_pf_zh, y_pf_ru = PEACH_MAP[y_idx]
    d_animal, d_pf_zh, d_pf_ru = PEACH_MAP[d_idx]

    y_sector, y_degrees = SECTOR[y_animal]
    d_sector, d_degrees = SECTOR[d_animal]

    lines = [
        "🌸 <b>Цветок Персика (桃花)</b>",
        "",
        f"📅 <b>Дата рождения:</b> {birth_date.strftime('%d.%m.%Y')}",
        f"🕐 <b>Время рождения:</b> {birth_time.strftime('%H:%M')}",
        f"🏙 <b>Город:</b> {city}",
        f"🐉 <b>Ветвь года:</b> {y_zh} {y_ru}  |  📆 <b>Ветвь дня:</b> {d_zh} {d_ru}",
        "",
    ]

    lines += _animal_block(y_animal, "года", y_pf_zh, y_pf_ru, y_sector, y_degrees, YEAR_NOTE)

    if y_animal == d_animal:
        lines += [
            "",
            f"✨ <b>Цветок Персика года и дня совпадают — {y_animal}.</b>",
            "• Ваша привлекательность и романтическая удача особенно сильны",
            "• Энергия действует стабильно — и в социальной жизни, и в личных отношениях",
            "• Вы обладаете двойной силой этого типа обаяния",
            "• Шансы на счастливые отношения значительно выше",
        ]
    else:
        lines += [""]
        lines += _animal_block(d_animal, "дня", d_pf_zh, d_pf_ru, d_sector, d_degrees, DAY_NOTE)
        lines += [
            "",
            f"💫 <b>У вас два разных Цветка Персика.</b>",
            f"<b>{y_animal}</b> (год) — ваш общий социальный магнетизм и харизма.",
            f"<b>{d_animal}</b> (день) — характер ваших личных, интимных отношений.",
        ]

    return "\n".join(lines), y_animal, d_animal, y_zh, d_zh


def get_activation_only(birth_date: date, birth_time: time, city: str) -> str:
    """Только блок активизации (для повторного запроса)."""
    y_idx, d_idx = _get_branches(birth_date, birth_time)
    y_animal = PEACH_MAP[y_idx][0]
    d_animal = PEACH_MAP[d_idx][0]
    y_sector = SECTOR[y_animal][0]
    d_sector = SECTOR[d_animal][0]

    parts = [y_sector]
    if y_animal != d_animal:
        parts.append(d_sector)
    return "\n".join(parts)
